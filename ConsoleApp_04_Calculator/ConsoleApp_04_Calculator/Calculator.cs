/*
 Create a Console Application to display the sum total when two double are passed into the Add method of the Calculator class.
 The Calculator class should have: 
 Add() method. This method should return a double of the sum when two double are passed in.
 */

namespace ConsoleApp_04_Calculator
{
    class Calculator
    {
        internal static double Add(double value1, double value2)
        {
            return value1 + value2;
        }
    }
}
